//Logical Operators
//&& both conditions must satisfy
console.log(true && true)
console.log(false && false)
console.log(true && false)
console.log(false && true)

//|| any one condition must satisfy
console.log(true || true)
console.log(false || false)
console.log(true || false)
console.log(false || true)

a = 10
b = 20
console.log(a && b) // 20

a = 10
b = 20
console.log(a || b) // 10