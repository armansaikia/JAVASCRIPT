//If we want to change the name we have two properties 1. Dot Notation 2. Bracket Notation

// Dot Notation

let person = {
    name: 'Arman',
    age: 22
};
  
 person.name = 'Arsish';
console.log(person)


//Bracket Notation

let person = {
    name: 'Arman',
    age: 22
};
  
 person['name'] = 'Lima';
console.log(person)